    Positive,

    /// <summary>
    /// Negative sentiment - indicates unfavorable or pessimistic content
    /// </summary>
    Negative,

    /// <summary>
    /// Neutral sentiment - indicates balanced or objective content
    /// </summary>
    Neutral
}